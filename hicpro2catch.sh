function usage {
    echo -e "usage : hicpro2catch.sh -i INPUT -b BINSIZE -c CHROM [-h]"
    echo -e "Use option -h|--help for more information"
}

function help {
    usage;
    echo 
    echo "Generate CaTCH suitable format from HiCPro output"
    echo "See https://github.com/zhanyinx/CaTCH_R for details about CaTCH"
    echo "---------------"
    echo "OPTIONS"
    echo
    echo "   -i|--input INPUT : .matrix file generated by HiC-Pro"
    echo "   -b|--binsize BINSIZE : The binning size of the hicpro matrix"
    echo "   -c|--chrom CHROM : File containing chromosomes size"
    echo "   [-h|--help]: help"
    exit;
}


if [ $# -lt 3 ]
then
    usage
    exit
fi

# Transform long options to short ones
for arg in "$@"; do
  shift
  case "$arg" in
      "--input") set -- "$@" "-i" ;;
      "--binsize")   set -- "$@" "-b" ;;
      "--chrom")   set -- "$@" "-c" ;;
      "--help")   set -- "$@" "-h" ;;
       *)        set -- "$@" "$arg"
  esac
done

chrfile=""
bin=""
file=""

while getopts ":i:b:c:h" OPT
do
    case $OPT in
        i) file=$OPTARG;;
        b) bin=$OPTARG;;
        c) chrfile=$OPTARG;;
        h) help ;;
        \?)
            echo "Invalid option: -$OPTARG" >&2
            usage
            exit 1
            ;;
        :)
            echo "Option -$OPTARG requires an argument." >&2
            usage
            exit 1
            ;;
    esac
done

if [[ -z $file ]];
then
    usage
    exit
fi


if [[ -z $chrfile ]];
then
    usage
    exit
fi


head -n 20 $chrfile > reduced_chromosome_file

awk 'BEGIN{bin="'"$bin"'"+0.;conta=0}{

	print $1,conta+1,conta+int($2/bin)+1
	conta=conta+int($2/bin)+1
			

}' reduced_chromosome_file > bin


awk 'BEGIN{fn=0; nchr=0}{

	if(FNR==1) fn++
	if(fn==1){
		chr[nchr]=$1;
		start[nchr]=$2;
		end[nchr]=$3;
		nchr++
	}

	if(fn==2){
		for(i=0;i<nchr;i++){
			if($1>=start[i] && $1<=end[i] && $2>=start[i] && $2<=end[i]){
				print chr[i],$1-start[i],$2-start[i],$3 > chr[i]".CaTCH" 
				print chr[i],$2-start[i],$1-start[i],$3 > chr[i]".CaTCH"
			}
		}
	}

}' bin $file > $file.catch


###
echo "The files *.CaTCH can be used as input for CaTCH"

rm reduced_chromosome_file bin
